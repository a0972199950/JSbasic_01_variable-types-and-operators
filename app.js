/*
【基本型別】
Number(數字)：1, 2, 3
String(字串)："apple", 'Estela', `Estela likes apple`
Boolean(布林值)：true, false
null(空值) => 當想要一個變數，但還沒決定裡面要放什麼的時候用的
undefined(未定義) => 所有變數的初始值
NaN(非數字) => 用數字的運算方式來算非數字時
*/


/*
【運算子】
算數運算子(回傳一個值)：+, -, *, /, %
關係運算子(回傳true或false)：>, <, >=, <=, ==, ===, !=, !==
邏輯運算子(回傳true或false)：&&, ||, !
賦值運算子(把右邊的值設定給左邊)：=
*/


/*
【一些基本的API】
console.log(【任何東西】)：把內容印到主控台上
alert(【任何東西】)：叫出瀏覽器警告視窗
prompt(【任何東西】)：叫出瀏覽器輸入框，回傳字串或null
confirm(【任何東西】)：叫出瀏覽器確認視窗，回傳true或false
Number(【字串】)：把字串轉換成數字
Math.random(【無】)：回傳一個隨機生成的0~0.9999...之間的數字
Math.floor(【數字】)：把數字做無條件捨去
Math.ceil(【數字】)：把數字做無條件進位
Math.round(【數字】)：把數字做四捨五入
*/